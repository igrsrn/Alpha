'use strict'

    function sum(a,b){
        return a + b;
    }

    function dif(a,b){
        return a - b;
    }

    function multiple(a,b){
        return a * b;
    }

    function devision(a,b){
        return a / b;
    }

    console.log(sum(4,5));

    console.log(dif(7,8));

    console.log(multiple(3,6));

    console.log(devision(2,8))